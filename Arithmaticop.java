package Operator1;

public class Arithmaticop {

	public static void main(String[] args) {
		int a = 20;
	    int b = 30;
		   System.out.println(a+b);
		   System.out.println(a-b);
		   System.out.println(a*b);
		   System.out.println(a/b);
		   System.out.println(a%b);
		   
		   System.out.println(a<b&&b<a);
		   System.out.println(a>b&&b<a);
		   System.out.println(a<b||b<a);
		   System.out.println(a>b||b<a);
		   
			System.out.println(a++ + ++a);
			System.out.println(b-- + --b);


	}

}
